    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if(!txtUsuario.getText().isEmpty() && !txtContraseña.getText().isEmpty()){
            Usuarios usuario  = new Usuarios(txtUsuario.getText(), txtContraseña.getText());
            if(Usuarios3.insertar(usuario)){
                JOptionPane.showMessageDialog(this, "Usuario registrado");
            }else{JOptionPane.showMessageDialog(this, "Usuario ya existente");
            
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed
    }
    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if(!txtUsuario.getText().isEmpty() && !txtContraseña.getText().isEmpty()){
            Usuarios usuario  = new Usuarios(txtUsuario.getText(), txtContraseña.getText());
            if(Usuarios3.modificar(usuario)){
                JOptionPane.showMessageDialog(this, "Usuario modificado con exito");
            }else{JOptionPane.showMessageDialog(this, "Usuario no encontrado");
            
        }
    }//GEN-LAST:event_btnModificarActionPerformed
    }
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(!txtUsuario.getText().isEmpty()){
           if(Usuarios3.eliminar(txtUsuario.getText())){
                JOptionPane.showMessageDialog(this, "Usuario eliminado con exito");
            }else{JOptionPane.showMessageDialog(this, "Usuario no encontrado");
            
        }
    }                                            
            
    }//GEN-LAST:event_btnEliminarActionPerformed
    
